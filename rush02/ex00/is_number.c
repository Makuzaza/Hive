#include "ft.h"

int	is_number(const char *str)
{
	if (*str == '0' && *(str + 1) == '\0')
		return (1);
	if (*str == '\0' || *str == '0')
	{
		return (0);
	}
	if (str[0] == '0')
	{
		if (str[1] >= '0' && str[1] <= '9')
			return (0);
	}
	while (*str)
	{
		if (*str < '0' || *str > '9')
			return (0);
		str++;
	}
	return (1);
}
